"use client"

import * as React from "react"
import { type DialogProps } from "@radix-ui/react-dialog"
import { Command as CommandPrimitive } from "cmdk"
import { Search } from "lucide-react"

import { cn } from "@/lib/utils"
import {
  Dialog,
  DialogContent,
  type DialogPosition,
} from "@/components/ui/dialog"
import styles from "@/styles/components/ui/command.module.scss"

const Command = React.forwardRef<
  React.ElementRef<typeof CommandPrimitive>,
  React.ComponentPropsWithoutRef<typeof CommandPrimitive>
>(({ className, ...props }, ref) => (
  <CommandPrimitive
    ref={ref}
    className={cn(styles["command"], className)}
    {...props}
  />
))
Command.displayName = CommandPrimitive.displayName

interface CommandDialogProps extends DialogProps, DialogPosition {
  style?: React.CSSProperties
}

const CommandDialog = ({
  children,
  position = "default",
  ...props
}: CommandDialogProps) => {
  return (
    <Dialog {...props}>
      <DialogContent types={"command"} position={position} style={props.style}>
        <Command className={styles["ccd"]} shouldFilter={false}>
          {children}
        </Command>
      </DialogContent>
    </Dialog>
  )
}

